template <class T> bool cmin(T& a, const T& b) {
	return b < a ? a = b, 1 : 0;
}
template <class T> bool cmax(T& a, const T& b) {
	return a < b ? a = b, 1 : 0;
}

const int N = 2e5 + 5, inf = 1e18;

map<array<int, 10>, vector<int>> cubes;

int32_t main() {
	ios::sync_with_stdio(0);
	cin.tie(0);

	for (int i = 1; i <= 1e6; i++) {
		int cube = 1LL * i * i * i;
		int val = cube;
		array<int, 10> f;
		for (int j = 0; j < 10; j++)
			f[j] = 0;
		while (cube > 0) {
			f[cube % 10]++;
			cube /= 10;
		}
		cubes[f].push_back(val);
	}

	vector<int> valid_answers;

	for (auto& it : cubes) {
		if (sz(it.second) == 5) {
			int small = 2 * inf;
			for (int x : it.second) {
				cmin(small, x);
			}
			valid_answers.push_back(small);
		}
	}

	sort(ALL(valid_answers));
	see(valid_answers.front());
}
