int phi(int n) {
	int result = n;
	for (int i = 2; i * i <= n; i++) {
		if (n % i == 0) {
			while (n % i == 0)
				n /= i;
			result -= result / i;
		}
	}
	if (n > 1)
		result -= result / n;
	return result;
}

void solve() {
	int n, k;
	cin >> n >> k;

	if (phi(n) - 1 == k)
		cout << "YES\n";
	else
		cout << "NO\n";
}

